// ── Font rendering ────────────────────────────────────────────────────────────

function glyphRow(char: string, row: number): boolean[] {
  const code = char.charCodeAt(0);
  const idx = (code < 32 || code > 126) ? 0 : code - 32;
  const byte = FONT_DATA[idx * FONT_H + row];
  const pixels: boolean[] = [];
  for (let bit = 7; bit >= 0; bit--) {
    pixels.push(((byte >> bit) & 1) === 1);
  }
  return pixels;
}

/** Text runs along the label feed axis (90 mm horizontal on 29×90 die-cut landscape). */
function renderTextAlongRows(
  canvas: MonoGrid,
  startRow: number,
  startCol: number,
  text: string,
  scale: number,
): number {
  const cleaned = text.replace(/[^\x20-\x7E]/g, "?");
  let row = startRow;

  for (let ci = 0; ci < cleaned.length; ci++) {
    for (let gr = 0; gr < FONT_H; gr++) {
      const pixels = glyphRow(cleaned[ci], gr);
      for (let sr = 0; sr < scale; sr++) {
        const r = row + gr * scale + sr;
        if (r >= canvas.length) return row;
        for (let px = 0; px < FONT_W; px++) {
          for (let sc = 0; sc < scale; sc++) {
            const c = startCol + px * scale + sc;
            if (c < canvas[0].length && pixels[px]) canvas[r][c] = true;
          }
        }
      }
    }
    row += FONT_W * scale;
  }

  return row;
}

function truncateForRow(text: string, startRow: number, scale: number, maxRows: number): string {
  const charW = FONT_W * scale;
  const budget = maxRows - startRow - 8;
  const maxChars = Math.max(4, Math.floor(budget / charW));
  const cleaned = text.replace(/[^\x20-\x7E]/g, "?").trim();
  if (cleaned.length <= maxChars) return cleaned;
  return `${cleaned.slice(0, Math.max(1, maxChars - 1))}…`;
}

function blitLogoToLandscape(
  canvas: MonoGrid,
  sprite: MonoGrid,
  gridRowStart: number,
  gridColStart: number,
  rowSpan: number,
  colSpan: number,
): void {
  const imgH = sprite.length;
  const imgW = sprite[0]?.length ?? 0;
  if (!imgW || !imgH) return;

  for (let dr = 0; dr < rowSpan; dr++) {
    const gr = gridRowStart + dr;
    if (gr >= canvas.length) break;
    const sx = Math.min(imgW - 1, Math.floor((dr * imgW) / rowSpan));
    for (let dc = 0; dc < colSpan; dc++) {
      const gc = gridColStart + dc;
      if (gc >= canvas[0].length) break;
      const sy = Math.min(imgH - 1, Math.floor((dc * imgH) / colSpan));
      if (sprite[sy][sx]) canvas[gr][gc] = true;
    }
  }
}

/**
 * Visitor badge matching Brother template (ace printer .lbxs):
 * landscape 29×90 mm — logo left, Name / Company / Email / Date of visit on the right.
 */
function renderVisitorBadgeGrid(fields: VisitorBadgeFields): boolean[][] {
  const rows = LABEL.rasterLines;
  const cols = LABEL.printPxWidth;
  const canvas: MonoGrid = Array.from({ length: rows }, () => new Array(cols).fill(false));

  const layout = BADGE_LAYOUT;
  const scale = layout.fontScale;

  const logo = loadVisitorLogoGrid(layout.logoRowSpan, layout.logoColSpan);
  if (logo.length) {
    blitLogoToLandscape(
      canvas,
      logo,
      layout.logoRow,
      layout.logoCol,
      layout.logoRowSpan,
      layout.logoColSpan,
    );
  }

  const lines: Array<{ col: number; text: string }> = [
    { col: layout.nameCol, text: `Name: ${fields.name}` },
    { col: layout.companyCol, text: `Company: ${fields.company}` },
    { col: layout.emailCol, text: `Email: ${fields.email}` },
    { col: layout.visitDateCol, text: `Date of visit: ${fields.visitDate}` },
  ];

  for (const line of lines) {
    const value = truncateForRow(line.text, layout.textRow, scale, rows);
    if (value) renderTextAlongRows(canvas, layout.textRow, line.col, value, scale);
  }

  return canvas;
}

function renderPixelGrid(lines: string[]): boolean[][] {
  const printPxWidth = LABEL.printPxWidth;
  const glyphH = FONT_H * FONT_SCALE;
  const gapH   = LINE_SPACING * FONT_SCALE;
  const rows: boolean[][] = [];

  for (let i = 0; i < MARGIN_TOP; i++) rows.push(new Array(printPxWidth).fill(false));

  for (let li = 0; li < lines.length; li++) {
    const line    = lines[li].replace(/[^\x20-\x7E]/g, '?');
    const charW   = FONT_W * FONT_SCALE;
    const textPx  = line.length * charW;
    const xOff    = Math.max(0, Math.floor((printPxWidth - textPx) / 2));

    for (let gr = 0; gr < FONT_H; gr++) {
      for (let sr = 0; sr < FONT_SCALE; sr++) {
        const row = new Array<boolean>(printPxWidth).fill(false);
        let x = xOff;
        for (let ci = 0; ci < line.length && x < printPxWidth; ci++) {
          const pixels = glyphRow(line[ci], gr);
          for (let px = 0; px < FONT_W && x < printPxWidth; px++) {
            for (let sc = 0; sc < FONT_SCALE && x < printPxWidth; sc++) {
              row[x++] = pixels[px];
            }
          }
        }
        rows.push(row);
      }
    }

    if (li < lines.length - 1) {
      for (let i = 0; i < gapH; i++) rows.push(new Array(printPxWidth).fill(false));
    }
  }

  for (let i = 0; i < MARGIN_BOTTOM; i++) rows.push(new Array(printPxWidth).fill(false));

  return rows;
}

function padGridToRasterHeight(grid: boolean[][]): boolean[][] {
  const width = LABEL.printPxWidth;
  const targetHeight = LABEL.dieCut ? LABEL.rasterLines : grid.length;
  if (grid.length >= targetHeight) return grid.slice(0, targetHeight);

  const topPad = Math.floor((targetHeight - grid.length) / 2);
  const blankRow = (): boolean[] => new Array(width).fill(false);
  return [
    ...Array.from({ length: topPad }, blankRow),
    ...grid,
    ...Array.from({ length: targetHeight - topPad - grid.length }, blankRow),
  ];
}

// ── Brother QL Raster packet builder ─────────────────────────────────────────

function buildRasterPacket(pixelGrid: boolean[][]): Buffer {
  const parts: Buffer[] = [];
  const printPxWidth = LABEL.printPxWidth;
  const grid = padGridToRasterHeight(pixelGrid);
  const rasterLines = grid.length;

  // 1. Invalidate — clear any partial command in the printer buffer
  parts.push(Buffer.alloc(200, 0x00));

  // 2. Initialize
  parts.push(Buffer.from([0x1B, 0x40]));

  // 3. Switch to raster mode
  parts.push(Buffer.from([0x1B, 0x69, 0x61, 0x01]));

  // 4. Set media and quality (ESC i z) — 10-byte Brother raster header
  //    0x8E = media type + width + length + quality valid
  parts.push(Buffer.from([
    0x1B, 0x69, 0x7A,
    0x8E,
    LABEL.mediaType,
    LABEL.mediaWidthMm,
    LABEL.mediaLengthMm,
    rasterLines & 0xFF,
    (rasterLines >> 8) & 0xFF,
    (rasterLines >> 16) & 0xFF,
    (rasterLines >> 24) & 0xFF,
    0x00, // starting page
    0x00,
  ]));

  // 5. Cut at end (die-cut labels are pre-sized; feed only)
  parts.push(Buffer.from([0x1B, 0x69, 0x4B, 0x08]));

  // 6. Set margins (feed) = 0
  parts.push(Buffer.from([0x1B, 0x69, 0x64, 0x00, 0x00]));

  // 7. Raster rows
  const bytesPerRow = Math.ceil(printPxWidth / 8);

  for (const row of grid) {
    const allBlank = !row.some(Boolean);
    if (allBlank) {
      parts.push(Buffer.from([0x5A]));
    } else {
      const rowBytes = Buffer.alloc(bytesPerRow, 0x00);
      for (let x = 0; x < printPxWidth; x++) {
        if (row[x]) {
          const byteIdx = Math.floor(x / 8);
          const bitPos  = 7 - (x % 8);
          rowBytes[byteIdx] |= (1 << bitPos);
        }
      }
      parts.push(Buffer.from([0x67, 0x00, bytesPerRow]));
      parts.push(rowBytes);
    }
  }

  // 8. Print with feed
  parts.push(Buffer.from([0x1A]));

  return Buffer.concat(parts);
}

// ── TCP send ─────────────────────────────────────────────────────────────────

function sendOverTcp(ip: string, port: number, data: Buffer): Promise<void> {
  return new Promise<void>((resolve, reject) => {
    const socket = new net.Socket();
    let settled  = false;
    const done   = (err?: Error) => {
      if (settled) return;
      settled = true;
      try { socket.destroy(); } catch {}
      err ? reject(err) : resolve();
    };

    socket.setTimeout(8000, () => done(new Error('Print socket timeout')));
    socket.once('error', done);

    socket.connect(port, ip, () => {
      socket.write(data, (err) => {
        if (err) return done(err);
        setTimeout(() => done(), 400);
      });
    });
  });
}

