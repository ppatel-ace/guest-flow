function buildRasterPacket(pixelGrid: boolean[][]): Buffer {
  const parts: Buffer[] = [];

  // 1. Invalidate — clear any partial command in the printer buffer
  parts.push(Buffer.alloc(200, 0x00));

  // 2. Initialize
  parts.push(Buffer.from([0x1B, 0x40]));

  // 3. Switch to raster mode
  parts.push(Buffer.from([0x1B, 0x69, 0x61, 0x01]));

  // 4. Set media and quality (ESC i z)
  //    flag=0x8E: print-quality + media-type + media-width + media-length valid
  //    mediaType=0x0A: continuous roll  width=62mm  length=0 (continuous)
  const numRows = pixelGrid.length;
  parts.push(Buffer.from([
    0x1B, 0x69, 0x7A,
    0x8E,
    0x0A,
    62,
    0,
    numRows & 0xFF,
    (numRows >> 8) & 0xFF,
    0x00, 0x00, 0x00, 0x00,
  ]));

  // 5. Auto-cut after each label
  parts.push(Buffer.from([0x1B, 0x69, 0x4B, 0x08]));

  // 6. Set margins (feed) = 0
  parts.push(Buffer.from([0x1B, 0x69, 0x64, 0x00, 0x00]));

  // 7. Raster rows
  //    696 dots → 87 bytes per row (696 / 8 = 87)
  const bytesPerRow = Math.ceil(PRINT_PX_WIDTH / 8);

  for (const row of pixelGrid) {
    const allBlank = !row.some(Boolean);
    if (allBlank) {
      parts.push(Buffer.from([0x5A]));           // empty-row shorthand
    } else {
      const rowBytes = Buffer.alloc(bytesPerRow, 0x00);
      for (let x = 0; x < PRINT_PX_WIDTH; x++) {
        if (row[x]) {
          const byteIdx = Math.floor(x / 8);
          const bitPos  = 7 - (x % 8);
          rowBytes[byteIdx] |= (1 << bitPos);
        }
      }
      parts.push(Buffer.from([0x67, 0x00, bytesPerRow]));
      parts.push(rowBytes);
    }
  }

  // 8. Print with feed
  parts.push(Buffer.from([0x1A]));

  return Buffer.concat(parts);
}

// ── TCP send ─────────────────────────────────────────────────────────────────

function sendOverTcp(ip: string, port: number, data: Buffer): Promise<void> {
  return new Promise<void>((resolve, reject) => {
    const socket = new net.Socket();
    let settled  = false;
    const done   = (err?: Error) => {
      if (settled) return;
      settled = true;
      try { socket.destroy(); } catch {}
      err ? reject(err) : resolve();
    };

    socket.setTimeout(8000, () => done(new Error('Print socket timeout')));
    socket.once('error', done);

    socket.connect(port, ip, () => {
      socket.write(data, (err) => {
        if (err) return done(err);
        setTimeout(() => done(), 400);
      });
    });
  });
}

// ── Public API ────────────────────────────────────────────────────────────────

/**
 * Print a label on the given Brother QL printer.
 * @param printer  Printer record from the database (must have ipAddress set)
 * @param lines    Text lines to render, centred on the 62mm label
 */
export async function printLabel(printer: Printer, lines: string[]): Promise<void> {
  const ip   = (printer as any).ipAddress as string | undefined;
  const port = ((printer as any).port as number | undefined) ?? DEFAULT_PORT;

  if (!ip) throw new Error("Printer has no ipAddress configured");

  const pixelGrid = renderPixelGrid(lines);
  const packet    = buildRasterPacket(pixelGrid);

  await sendOverTcp(ip, port, packet);
}

export default { printLabel };
