/**
 * Render the visitor badge as a column-major monochrome grid.
 * Returns boolean[W][H] where W = feed direction, H = tape width.
 */
export async function renderVisitorBadgeGrid(
  fields: VisitorBadgeFields,
): Promise<MonoGrid> {
  const grid: MonoGrid = Array.from({ length: W }, () => new Array(H).fill(false));

  await stampLogo(grid);

  FIELDS.forEach(({ label, key }, i) => {
    const value = (fields[key] ?? "").replace(/[^\x20-\x7E]/g, "?").trim();
    const text  = value ? `${label}: ${value}` : `${label}:`;
    const y     = TEXT_START + i * LINE_PITCH;
    drawText(grid, text, TEXT_X, y);
  });

  return grid;
}
