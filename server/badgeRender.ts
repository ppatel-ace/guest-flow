// ── Font helpers ──────────────────────────────────────────────────────────────

function glyphRow(char: string, row: number): boolean[] {
  const code = char.charCodeAt(0);
  const idx  = (code < 32 || code > 126) ? 0 : code - 32;
  const byte = FONT_DATA[idx * FONT_H + row];
  const out: boolean[] = [];
  for (let bit = 7; bit >= 0; bit--) out.push(((byte >> bit) & 1) === 1);
  return out;
}

/**
 * Draw a string of text into the grid at pixel position (px, py).
 * Characters that would fall outside the canvas are silently clipped.
 */
function drawText(
  grid:  MonoGrid,
  text:  string,
  px:    number,
  py:    number,
): void {
  const safe = text.replace(/[^\x20-\x7E]/g, "?");
  const maxChars = Math.max(0, Math.floor((W - px) / CHAR_W));
  const clipped  = safe.slice(0, maxChars);

  for (let ci = 0; ci < clipped.length; ci++) {
    const charX = px + ci * CHAR_W;
    for (let gr = 0; gr < FONT_H; gr++) {
      const pixels = glyphRow(clipped[ci], gr);
      for (let sr = 0; sr < FONT_SCALE; sr++) {
        const gy = py + gr * FONT_SCALE + sr;
        if (gy < 0 || gy >= H) continue;
        for (let px2 = 0; px2 < FONT_W; px2++) {
          for (let sc = 0; sc < FONT_SCALE; sc++) {
            const gx = charX + px2 * FONT_SCALE + sc;
            if (gx >= 0 && gx < W && pixels[px2]) {
              grid[gx][gy] = true;
            }
          }
        }
      }
    }
  }
}

// ── Logo loader ───────────────────────────────────────────────────────────────

/**
 * Load the ACE Electronics logo, threshold to monochrome, and stamp it into
 * the grid at (LOGO_X, LOGO_Y).  Works with BMP, PNG, or JPG inputs.
 */
async function stampLogo(grid: MonoGrid): Promise<void> {
  const logoPath =
    resolveAssetPath("ace-visitor-logo.bmp") ??
    resolveAssetPath("ace-visitor-logo.png") ??
    resolveAssetPath("ace-visitor-logo.jpg");

  if (!logoPath) {
    console.warn("[badge] No logo file found — label will print without logo");
    return;
  }

  let img: GrayImage;
  try {
    const raw = await loadLogoGrayscale(logoPath);
    img = resizeGrayscaleNN(raw, LOGO_MAX, LOGO_MAX);
  } catch (err) {
    console.warn("[badge] Logo load failed, printing without logo:", (err as Error).message);
    return;
  }

  // Centre the resized logo within the logo zone
  const offsetX = LOGO_X + Math.floor((LOGO_MAX - img.width)  / 2);
  const offsetY = LOGO_Y + Math.floor((LOGO_MAX - img.height) / 2);

  for (let ly = 0; ly < img.height; ly++) {
    for (let lx = 0; lx < img.width; lx++) {
      const lum = img.data[ly * img.width + lx];
      if (lum < 128) {
        const gx = offsetX + lx;
        const gy = offsetY + ly;
        if (gx >= 0 && gx < W && gy >= 0 && gy < H) {
          grid[gx][gy] = true;
        }
      }
    }
  }
}

